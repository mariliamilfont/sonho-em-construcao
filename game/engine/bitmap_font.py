def draw_text_bitmap(x, y, text, color, set_pixel_fn, scale=2):
    cursor_x = x

    for ch in text.upper():
        glyph = BITMAP_FONT.get(ch, BITMAP_FONT[" "])
        for gy, row in enumerate(glyph):
            for gx, bit in enumerate(row):
                if bit == "1":
                    for sy in range(scale):
                        for sx in range(scale):
                            set_pixel_fn(cursor_x + gx * scale + sx, y + gy * scale + sy, color)
        cursor_x += (5 * scale) + scale


def bitmap_text_width(text, scale=2):
    if not text:
        return 0
    return (len(text) * (5 * scale + scale)) - scale


def draw_text_bitmap_centered(y, text, color, width, set_pixel_fn, scale=2):
    text_w = bitmap_text_width(text, scale)
    x = (width - text_w) // 2
    draw_text_bitmap(x, y, text, color, set_pixel_fn, scale)
